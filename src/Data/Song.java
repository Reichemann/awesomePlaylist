package Data;

public class Song {
    private String title;
    private String artistName;
    private String albumName;
    private String year;

    public String getTitle() {
        return title;
    }

    public String getArtistName() {
        return artistName;
    }

    public String getAlbumName() {
        return albumName;
    }

    public String getYear() {
        return year;
    }
}